/**
 * Represents the state of keyboard inputs for controlling the game.
 * This class keeps track of the state (pressed or not) of several keys
 * that are used for character movement and interactions.
 * 
 * @class Keyboard
 */
class Keyboard {

    /**
   * Indicates whether the arrow key is pressed.
   * 
   * @type {boolean}
   */
    LEFT = false;
    RIGHT = false;
    UP = false;
    DOWN = false;
    SPACE = false;
    SPACE_SOLVED= false;
    D = false; 
    D_SOLVED= false;
    S = false;
    S_SOLVED= false;
}